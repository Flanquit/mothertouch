                        <p>MTGS has over the past years been experiencing unprecedented growth in several aspects such as enrolment, staffing, infrastructure, educational standards and quality of our learners’ results amongst others. This has been compounded by the fact that we have an excellent and exceptionally rich and wide curriculum, conducive learning environment, well qualified, experienced staff and state of the art infrastructure and equipment, to mention just a few. </p>

                        <p> <h3>OUR CURRICULUM:</h3> 
                            In addition to <b>Zimsec</b> curriculum, we offer <b>Cambridge, Microsoft, HEXCO,</b> amongst others, so as to integrate, enrich, enhance and facilitate holistic learning and development to produce the extra ordinary young leaders equipped with relevant skills that empower them to be competent and relevant to the global village. We have continued to increase and diversify our curriculum to impart life skills that go beyond the classroom such as Entrepreneurship, Artificial and Financial intelligence, and offer wide range of Practical and Science subjects. We also offer foreign Languages like Chinese and French to allow our learners to fit in the global arena. We have also made sizeable investments to ensure that our schools have excellent ICT equipment and requirements to enable our learners to be conversant with technological trends to participate in the 21st century learning and development, connect and partner with other local, regional and international learners, sharing ideas during lesson and other times for relevance and success. 
                        </p>
                        <p>MTGS also offers world class Sporting facilities that cater for several disciplines such as Athletics, Basketball, Chess, Cricket, Golf, Hockey, Rugby, Soccer, Swimming, Tennis, Volleyball amongst others. Plans are underway to introduce other disciplines such as Baseball.  We also offer a wide range of relevant Extra Curricular activities such as Clubs, Arts to foster leadership, communication and other life skills. In addition, our learners get the opportunity to participate in several national, regional and international musical festivals like, the Elite Combined Schools Choir, National and international Musical Eisteddfod, and others. Our rich and wide curriculum caters for each individual learner and promote holistic learning and development from ECD to Senior School.</p>
                        <p>
                             <h3>OUR LEARNING ENVIRONMENT</h3>
                            The school is continuously increasing in terms of infrastructure, facilities and acquiring state of the art equipment to support the wide curriculum. We have managed to create a conducive environment that is safe, exciting, engaging and encourages creativity and innovation to enhance holistic development of the learner. MTGS has an environment that, both the parents and learners are proud to be part of. Our state of the art infrastructure encompasses four double storey classroom blocks, each block consisting of eight classrooms, an administration block, and warm, homely <b>boarding facilitie</b> with 2 hostels, namely <i>‘Bethel’</i> for boys and <i>‘Gebeon’</i> for girls. We also have <i>‘Mount Zion’</i>, our group of Schools Chapel which is meant to foster and enhance spiritual growth to our learners. As the word of God clearly commands us in <b>Proverbs 22:6 “Train a child in the right way and when he is old he will not depart from it.’</b> <i>This will enable us to produce a God fearing successful generation with godly wisdom.  We are also in the process of completing a1500 seater school auditorium. The Skills centre is one of our upcoming major project that will greatly cater for the learner’s needs and foster holistic development for relevance and success. Also upcoming is the ‘Garden of Eden’ that will provide a conducive beautiful, fresh, and healthy environment with excellent landscape, beautiful wide variety of flowers, organic, exotic and indigenous fruit trees, herbs and vegetables to list just a few. All this to benefit the learner and Mtgs with health food and fresh environment.</i>
                        </p>

                        <p><h3>OUR STAFF</h3>
                            Our staff compliment and retention has continuously increased and improved. Not only have we increased in quantity but also in quality by engaging more qualified, experienced, committed, competent and diligent staff across the board for the growth and development of the learner.  In addition, we have ensured that our workforce remains highly motivated to diligently carry out its duties by establishing systems that reward diligence, teamwork, innovation and continuous development for value addition and relevance in the 21st century teaching and learning.  Our staff remain our greatest and most valuable strategic resource that has, and will enable us to achieve our desired goals and results. 
                            
                        </p>
